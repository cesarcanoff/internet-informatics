<?php
class Animais{
	protected $nome;
	protected $raca;

	public function getNome() {
		return $this->nome;
	}

	public function setNome($nome): void {
		$this->nome = $nome;
	}

	public function getRaca() {
		$this->raca = $raca;
	}

	public function setRaca($raca): void {
		$this->raca = $raca;
	}
}
?>