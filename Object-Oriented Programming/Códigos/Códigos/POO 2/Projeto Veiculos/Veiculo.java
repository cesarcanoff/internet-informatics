
package Proj_Veiculos;

public class Veiculo {
    //Atributos.
    protected String placa;
    protected int ano;
    
    //Metodos Especiais.

    public String getPlaca() {
        return placa;
    }
    
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public int getAno() {
        return ano;
    }
    
    public void setAno(int ano) {
        this.ano = ano;
    }
    
}